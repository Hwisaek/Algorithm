func main() {
	defer w.Flush()
	s.Split(bufio.ScanLines)

	var A, B, C, D [5][3]byte

	for i := 0; i < 5; i++ {
		s := scan()
		A[i][0] = s[0]
		A[i][1] = s[1]
		A[i][2] = s[2]

		B[i][0] = s[4]
		B[i][1] = s[5]
		B[i][2] = s[6]

		C[i][0] = s[8]
		C[i][1] = s[9]
		C[i][2] = s[10]

		D[i][0] = s[12]
		D[i][1] = s[13]
		D[i][2] = s[14]
	}

	fmt.Fprintf(w, "%d%d:%d%d", check(A), check(B), check(C), check(D))
}

func check(a [5][3]byte) int {
	for c := 0; c < 10; c++ {
		match := true
		for i := 0; i < 5 && match; i++ {
			for j := 0; j < 3 && match; j++ {
				if a[i][j] == '#' && n[c][i][j] == '.' {
					match = false
				}
			}
		}
		if match {
			return c
		}
	}
	return -1
}
